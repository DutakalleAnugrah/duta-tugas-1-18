// operator aritmatika

// ekspresi dan statment

// */+-
// % <- modulo
// ++ <- increment
// --  <- decrement
// ** <- pangkat atau power

let x = 10
console.log(x + 1);
console.log(x - 1);
console.log(x / 4);
console.log(x * 2);

// aritmatika khusus di javascript
console.log(x % 3);
console.log(x  ** 2);

// pemgroman biasa
x = x + 1;
console.log(x)
x = x + 1;
console.log(x);
x++;
x++;
console.log(x);

x = x - 1;
console.log(x);
x--;
console.log(x)

